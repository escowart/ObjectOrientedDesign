package rbtree;


/** An interface representing a Red-Black Color 
 *  @author Edwin Cowart
 *  @version 2013.11.14 */
public interface IRBColor {
    /** Is this Red-Black Color a Black Color?
     * @return : A boolean of whether this is a Black color*/
    public boolean isBlack();
    
    /** Is this Red-Black Color a Red Color?
     * @return : A boolean of whether this is a Red color*/
    public boolean isRed();
}


